  <h3>Origem</h3>
  <p>Judaísmo (em hebraico: יהדות, Yahadút) é uma das três principais religiões abraâmicas, definida como "religião, filosofia e modo de vida" do povo judeu. Originário da Torá Escrita e da Bíblia Hebraica (também conhecida como Tanakh) e explorado em textos posteriores, como o Talmud, é considerado pelos judeus religiosos como a expressão do relacionamento e da aliança desenvolvida entre Deus com os Filhos de Israel. De acordo com o judaísmo rabínico tradicional, Deus revelou as suas leis e mandamentos a Moisés no Monte Sinai, na forma de uma Torá escrita e oral. Esta foi historicamente desafiada pelo caraítas, um movimento que floresceu no período medieval que mantém milhares de seguidores atualmente e, que afirma que apenas a Torá escrita foi revelada. Nos tempos modernos alguns movimentos liberais, tais como o judaísmo humanista, podem ser considerados não teístas.
</p><p>
Na sua essência, o Tanaque é um relato da relação dos israelitas com Deus desde sua história mais antiga até a construção do Segundo Templo (c. 535 a.C.). Abraão é saudado como o primeiro hebreu e o pai do povo judeu. Como recompensa por seu ato de fé em um Deus, foi prometido que Isaac, seu segundo filho, herdaria a Terra de Israel (então chamada Canaã). Mais tarde, os descendentes de Jacó (filho de Isaac) foram escravizados no Egito, que após séculos de escravidão Deus ordenou a Moisés que liderasse a libertação da escravidão do Egito, movimento conhecido por o Êxodo.

No Monte Sinai, eles receberam a Torá — os cinco livros de Moisés. Estes livros, juntos com Nevi'im e Ketuvim formam o Torah Shebikhtav em oposição à Torá Oral, que se refere a Mishná e ao Talmude. Deus em seguida levou-os para a terra de Israel, onde o tabernáculo foi implantado na cidade de Siló por mais de 300 anos para reunir a nação contra os ataques de inimigos. Conforme o tempo passava, o nível espiritual da nação recuou até o ponto em que Deus permitiu que os filisteus capturassem o tabernáculo.

O povo de Israel exigiu ao profeta Samuel um governo liderado por um rei permanente. Samuel então nomeou Saul para tal cargo. Quando o povo pressionou Saul em ir contra uma ordem transmitida a ele por Samuel, Deus disse a Samuel nomear Davi em seu lugar. Depois que o reinado de Davi foi estabelecido, ele informa ao profeta Natã seu desejo de construir um templo permanente. Como recompensa por seus atos, Deus prometeu a Davi que ele permitiria que seu filho, Salomão, construísse o Primeiro Templo e que o trono nunca seria afastado de seus filhos.
</p><p>
Alguns estudiosos críticos opõem-se à ideia de que os textos sagrados, incluindo a Bíblia Hebraica, foram divinamente inspirados. Muitos desses estudiosos aceitam os princípios gerais da hipótese documental e sugerem que a Torá é composta de textos incoerentes editados em conjunto de uma forma que chama a atenção para contos divergentes. Muitos sugerem que, durante o período do Primeiro Templo, o povo de Israel acreditava que cada nação tinha seu próprio deus, mas que seu deus era superior aos outros deuses. Alguns sugerem que o monoteísmo estrito desenvolveu-se durante o Exílio babilônico, talvez em reação ao dualismo do zoroastrismo. De acordo com esse ponto de vista, foi apenas no período Helênico que a maioria dos judeus passou a acreditar que seu Deus era o único deus e que a noção de uma nação judaica floresceu com base na religião judaica. John Day argumenta que as origens do Yahweh, El, Aserá e Baal, pode estar enraizada no início da religião cananeia, que era centrada em um panteão de deuses muito parecido com o panteão grego.
</p>
