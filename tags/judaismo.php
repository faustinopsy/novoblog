<div id="Variações" class="tabcontent">
  <h3>Variações</h3>
  <p>O judaísmo rabínico, surgido do movimento dos fariseus após a destruição do Segundo Templo, e que aceita a tradição oral além da Torá escrita, é o único que hoje em dia é reconhecido como judaísmo, e é comumente dividido nos seguintes movimentos:</p>
  <p><b>Judaísmo ortodoxo:</b></p>
  <p>considera que a Torá foi escrita por Deus que a ditou a Moisés, sendo as suas leis imutáveis. Os judeus ortodoxos consideram o Shulkhan Arukh (compilação das leis do Talmude do século XVI, pelo rabino Yosef Karo) como a codificação definitiva da lei judaica.[61] O judaísmo ortodoxo exprime-se informalmente através de dois grupos, o judaísmo moderno ortodoxo e o judaísmo haredi.</p>
    <p><b>Judaísmo conservador:</b></p>
  <p>fora dos Estados Unidos é conhecido por judaísmo Masorti. Desenvolveu-se na Europa e nos Estados Unidos no século XIX, em resultado das mudanças introduzidas pelo iluminismo e a Emancipação dos Judeus. Caracteriza-se por um compromisso em seguir as leis e práticas do judaísmo tradicional, como o Shabat e o cashrut, uma atitude positiva em relação à cultura moderna e uma aceitação dos métodos rabínicos tradicionais de estudo das escrituras, bem como o recurso a modernas práticas de crítica textual. Considera que o judaísmo não é uma fé estática, mas uma religião que se adapta a novas condições.</p>
    <p><b>Judaísmo reformista:</b></p>
  <p>formou-se na Alemanha em resposta ao iluminismo. Rejeita a visão de que a lei judaica deva ser seguida pelo indivíduo de forma obrigatória, afirmando a soberania individual sobre o que observar. De início este movimento rejeitou práticas como a circuncisão, dando ênfase aos ensinamentos éticos dos profetas; as orações eram realizadas na língua vernácula.</p>
  <p><b>Judaísmo reconstrucionista:</b></p>
  <p>formou-se entre as décadas de 20 e 40 do século XX por Mordecai Kaplan, um rabino inicialmente conservador que mais tarde deu ênfase à reinterpretação do judaísmo em termos contemporâneos. À semelhança do judaísmo reformista não considera que a lei judaica deva ser suprema, mas ao mesmo tempo considera que as práticas individuais devem ser tomadas no contexto do consenso comunal</p>
  <p><b>Judaísmo humanístico:</b></p>
  <p>O judaísmo humanístico é um movimento no judaísmo que busca manter a identidade cultural e tradição judaicas ao mesmo tempo que deixa de enfatizar crenças teísticas, é um movimento no judaísmo, que oferece uma alternativa não teísta na vida judaica contemporânea. Ele define o judaísmo como a experiência cultural e histórica do povo judeu e incentiva humanistas e seculares judeus para celebrar a sua identidade judaica através da participação em festas judaicas e eventos do ciclo de vida (como casamentos, bar e bat mitzvah) com cerimônias inspiradoras que se apoiam, mas ir além da literatura tradicional.</p>
  
