  <h3>Origem</h3>
  <p>"Islão" provem do árabe Islām, que por sua vez deriva da quarta forma verbal da raiz slm, aslama, e significa "submissão (a Alá)". Segundo o arabista e filólogo José Pedro Machado, a palavra "Islão" não teria surgido na língua portuguesa antes de 1843, ano em que aparece no capítulo IX da obra Eurico, o Presbítero, de Alexandre Herculano.

O Islão é descrito em árabe como um "diin", o que significa "modo de vida" e/ou "religião" e possui uma relação etimológica com outras palavras árabes como Salaam ou Shalam (Shalaam / Shalom), que significam "paz".

Muçulmano, por sua vez, deriva da palavra árabe muslim (plural, muslimún), particípio activo do verbo aslama, designando "aquele que se submete". O vocábulo pode ter penetrado no português a partir do castelhano, sendo provável que essa língua o tenha tomado do italiano ou do francês, línguas nas quais o vocábulo surge em 1619 e 1657, respectivamente (no primeiro caso como mossulmani, na obra Viaggi, de Pietro della Valle, e no segundo como mousulmans, na obra Voyages, de Le Gouz de la Boullaye).

Em textos mais antigos, os muçulmanos eram conhecidos como "maometanos", este termo tem vindo a cair em desuso porque implica, incorrectamente, que os muçulmanos adoram Maomé (como, durante alguns séculos, por completo desconhecimento, o Ocidente pensou), o que torna o termo ofensivo para muitos muçulmanos. Durante a Idade Média e, por extensão, nas lendas e narrativas populares cristãs, os muçulmanos eram também designados como sarracenos e também por mouros (embora este último termo designasse mais concretamente os muçulmanos naturais do Magrebe, que se encontravam na Península Ibérica).

Islão pode referir-se também ao conjunto de países que seguem esta religião (a jurisprudência islâmica utiliza nesse caso a expressão Dar-al-Islam, "casa do Islão").
</p>
<p>
Na tradição muçulmana, Maomé (c 570 - 8 de junho de 632) é visto como o último de uma série de profetas principais. Durante os últimos 22 anos de sua vida, começando aos 40 anos, em 610, de acordo para as primeiras biografias restantes, Maomé relatou revelações que ele acreditava serem de Deus, transmitidas a ele através do arcanjo Gabriel (Jibril). O conteúdo dessas revelações, conhecido como o Alcorão, foi memorizado e gravado por seus companheiros.


Profeta Maomé recitando o Alcorão em Meca (gravura do século XV)
Durante esta época, Maomé pregava ao povo na cidade de Meca, implorando-los a abandonar o politeísmo e adorar um Deus. Embora alguns tenham se convertido ao Islão, Maomé e seus seguidores foram perseguidos pelas autoridades de Meca. Isso resultou na migração para a Abissínia de alguns muçulmanos (ao Império Axumita). Muitos dos primeiros convertidos ao Islão eram os pobres e ex-escravos como Bilal Ibn Rabah al-Habashi. A elite de Meca acreditava que Maomé iria desestabilizar a ordem social através da pregação de uma religião monoteísta, da igualdade racial e do processo de dar ideias aos pobres e seus escravos.

Depois de 12 anos de perseguição de muçulmanos por os habitantes de Meca, Maomé, sua família e os primeiros muçulmanos realizaram a Hégira ("emigração") para a cidade de Medina (anteriormente conhecida como Iatrebe) em 622. Lá, com os convertidos de Medina (Ansar) e os migrantes de Meca (muhajirun), Maomé estabeleceu sua autoridade política e religiosa. Um Estado foi estabelecido em conformidade com a jurisprudência econômica islâmica. A Constituição de Medina foi formulada, instituindo uma série de direitos e responsabilidades para os muçulmanos, judeus, cristãos e para as comunidades pagãs de Medina, unindo-os dentro de uma comunidade - a Umma.
</p>
