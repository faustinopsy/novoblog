  <p>Os muçulmanos acreditam no quadar, uma palavra geralmente traduzida como "predestinação", mas cujo sentido mais preciso é "medir" ou "decidir quantidade ou qualidade". Uma vez que, para o islão, Deus foi o criador de tudo, incluindo dos seres humanos, e sendo uma das suas características a omnisciência, ele já sabia, quando procedeu à criação, as características que cada elemento da sua obra teria. Assim sendo, cada coisa que acontece a uma pessoa foi determinada por Deus.</p>
