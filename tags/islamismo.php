<div id="Variações" class="tabcontent">
  <h3>Variações</h3>

  <p><b>Sunismo</b></p>
  <p>O sunismo é a maior denominação do islão e representa 75%-90% de todos os muçulmanos. Os muçulmanos sunitas também são conhecidos pelo nome Ahl as-Sunnah, que significa "povo da tradição [de Maomé]". Estes hádices, contos, ações e características pessoais de Maomé, são preservados nas tradições conhecidas como Al-Kutub Al-Sittah (seis grandes livros).</p>
    <p><b>Xiismo</b></p>
  <p>Os xiitas constituem 10-20% dos muçulmanos e são o segundo maior do ramo do islão.

Enquanto os sunitas acreditam que um califa deve ser eleito pela comunidade, os xiitas acreditam que Maomé indicou seu genro, Ali, como seu sucessor e apenas certos descendentes de Ali poderiam ser imames (líderes). Como resultado disso, eles acreditam que Ali foi o primeiro imame, rejeitando a legitimidade dos califas muçulmanos anteriores Abacar, Otomão e Omar.</p>
    <p><b>RCarijitas/Ibadismo</b></p>
  <p>Outra denominação que tem origem nos tempos históricos do islão é a dos carijitas. Historicamente, consideravam que qualquer homem, independentemente da sua origem familiar, poderia ser líder da comunidade islâmica, opondo-se às polémicas de sucessão entre sunitas e xiitas. Os ideais carijitas ainda existem no mundo Islâmico, mesmo que de forma diferente da original, através do takfirismo, que é excomungar alguém do islão por ter cometido um pecado grave. Como as formas de julgamento desses grupos são extremamente subjetivas e não tomam por base a aplicação correta da Charia, esses grupos se tornam extremamente violentos contra muçulmanos e não muçulmanos.</p>
  <p><b>Sufismo</b></p>
  <p>Às vezes visto pelos fiéis muçulmanos comuns como um ramo separado do islão, o sufismo é antes uma forma de mística que pretende alcançar um contacto direto com Deus através de uma série de práticas que geralmente incluem o ascetismo, a meditação, os jejuns, cantos e danças.

Desconhece-se de onde deriva a palavra sufismo (em árabe: tasawwuf). O termo poderá provir de sūf, "lã", o que se encontra relacionado com o facto de os primeiros sufis vestirem roupas feitas com o material, imitando os ascetas cristãos da Síria e da Palestina. Outra teoria procura relacionar sufismo com a palavra árabe safa, que significa "pureza".

O sufismo já existia como movimento no primeiro século do islão. Para os sufis, o próprio profeta Maomé seria um deles, já que levaria uma vida extremamente simples, tendo por hábito retirar-se de Meca para meditar numa caverna, tendo estabelecido uma relação próxima com Deus..</p>
